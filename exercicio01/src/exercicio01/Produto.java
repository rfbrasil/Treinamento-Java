package exercicio01;

public class Produto {
	
	private String nome;
	private double valor;
	
	//Seta get e set dos objetos
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	
	

}
